# =========================================================
# 1. WHAT ARE COMMENTS?
# =========================================================

"""
Comments are used to explain Python code.

They help to:
- Make code more readable
- Explain logic to others
- Prevent execution of code during testing
"""

# =========================================================
# 2. SINGLE-LINE COMMENTS
# =========================================================

"""
Single-line comments start with #.
Python ignores everything after # on that line.
"""

# This is a comment
print("Hello, World!")

# =========================================================
# 3. COMMENTS AT THE END OF A LINE
# =========================================================

"""
Comments can also be placed at the end of a line.
"""

print("Hello, World!")  # This is a comment

# =========================================================
# 4. USING COMMENTS TO DISABLE CODE
# =========================================================

"""
Comments can be used to prevent Python from executing code.
This is very useful while testing or debugging.
"""

# print("Hello, World!")   # This line will not execute
print("Cheers, Mate!")

# =========================================================
# 5. MULTI-LINE COMMENTS (USING #)
# =========================================================

"""
Python does not have a special syntax for multiline comments.

The most common way is to use # at the beginning of each line.
"""

# This is a comment
# written in
# more than just one line
print("Hello, World!")

# =========================================================
# 6. MULTI-LINE COMMENTS (USING TRIPLE QUOTES)
# =========================================================

"""
Another way to write multiline comments is by using
multi-line strings (triple quotes).

IMPORTANT:
- These are actually strings
- If not assigned to a variable, Python ignores them
"""

"""
This is a comment
written in
more than just one line
"""

print("Hello, World!")

# =========================================================
# 7. WHEN TO USE WHICH COMMENT STYLE
# =========================================================

"""
Best Practices:
- Use # for single-line comments
- Use # for real comments in production code
- Triple-quoted strings are mainly used for documentation
"""

# =========================================================
# 8. SUMMARY
# =========================================================

"""
KEY POINTS:
- Comments explain code
- Comments improve readability
- Comments can disable code execution
- # is used for single-line comments
- Multiline comments use multiple # or triple quotes

"""
