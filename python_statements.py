# =========================================================
# 7. BEST PRACTICE (IMPORTANT FOR STUDENTS)
# =========================================================

"""
Best Practice:
- Write one statement per line
- Keep code clean and readable
- Avoid semicolons unless really needed
"""

print("Best practice example 1")
print("Best practice example 2")
print("Best practice example 3")

