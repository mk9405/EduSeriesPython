# =========================================================
# 1. WHAT ARE PYTHON STATEMENTS?
# =========================================================

"""
A computer program is a list of instructions
that are executed by the computer.

In Python (and other programming languages),
these instructions are called STATEMENTS.
"""

# Example of a Python statement
print("Python is fun!")

# =========================================================
# 2. END OF A STATEMENT IN PYTHON
# =========================================================

"""
In Python, a statement usually ends when the line ends.

IMPORTANT:
- No semicolon (;) is required
- New line indicates end of statement
"""

print("This is one statement")
print("This is another statement")

# =========================================================
# 3. MANY STATEMENTS IN A PROGRAM
# =========================================================

"""
Most Python programs contain many statements.
They are executed one by one, in the same order
as they are written.
"""

print("Hello World!")
print("Have a good day.")
print("Learning Python is fun!")

# =========================================================
# 4. HOW STATEMENTS ARE EXECUTED (EXPLANATION)
# =========================================================

"""
Execution order:
1. First statement runs
2. Second statement runs
3. Third statement runs

Python always follows top-to-bottom execution.
"""

# =========================================================
# 5. SEMICOLONS IN PYTHON (OPTIONAL)
# =========================================================

"""
Semicolons (;) are OPTIONAL in Python.

You can write multiple statements on the same line
using semicolons, but this is rarely used.
Reason: It reduces readability.
"""

# Multiple statements in one line (NOT recommended)
print("Hello"); print("How are you?"); print("Bye bye!")

# =========================================================
# 6. INVALID STATEMENT EXAMPLE
# =========================================================

"""
If you write multiple statements on the same line
WITHOUT a separator (newline or semicolon),
Python will raise a SyntaxError.
"""

